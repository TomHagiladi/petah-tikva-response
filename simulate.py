def reset_db():
    banner("שלב 1/8: ניקוי מסד הנתונים")
    keys = ["participants", "groups", "mood", "moodRequest", "moodSummary",
            "actions", "actionsRequest", "actionsSummary",
            "feedback", "summaryRequest", "summary", "photos"]
    for k in keys:
        db_delete(k)
    ok(f"נמחקו {len(keys)} keys")


def register_participants():
    banner("שלב 2/8: רישום 32 משתתפים")
    uids = []
    for idx, (name, gender) in enumerate(PARTICIPANTS):
        uid = f"sim-{idx:02d}-{uuid.uuid4().hex[:6]}"
        db_put(f"participants/{uid}", {
            "name": name,
            "gender": gender,
            "joinedAt": now_ms(),
            "currentScreen": "screen-waiting",
        })
        uids.append(uid)
    males = sum(1 for _, g in PARTICIPANTS if g == "male")
    females = sum(1 for _, g in PARTICIPANTS if g == "female")
    ok(f"נרשמו {len(uids)} משתתפים ({males} גברים, {females} נשים)")
    return uids


def wait_for_groups():
    banner("שלב 3/8: חלוקה לקבוצות")
    info(f"דשבורד: {DASHBOARD_URL}")
    pause("פתח את הדשבורד, בחר גודל קבוצה (3 או 4), ולחץ \"חלוקה לקבוצות\".")

    groups = db_get("groups")
    if not groups:
        err("לא נמצאו קבוצות ב-/groups. בטל את הסקריפט ונסה שוב.")
        sys.exit(1)
    sizes = sorted([len(g.get("members", [])) for g in groups.values()], reverse=True)
    ok(f"נוצרו {len(groups)} קבוצות בגדלים: {sizes}")

    # Verify all 32 are assigned
    parts = db_get("participants") or {}
    assigned = sum(1 for p in parts.values() if p.get("group"))
    if assigned != 32:
        err(f"רק {assigned}/32 קיבלו group. בדוק.")
    else:
        ok("כל 32 המשתתפים שובצו לקבוצות")
    return groups


def submit_pipeline(kind, texts, uids, label, step_num):
    banner(f"שלב {step_num}/8: שליחת {len(texts)} תגובות לפייפליין '{label}'")
    parts = db_get("participants") or {}
    sent = 0
    for uid, text in zip(uids, texts):
        p = parts.get(uid, {})
        group = p.get("group")
        body = {
            "text": text,
            "group": group,
            "submittedAt": now_ms(),
        }
        if kind == "actions":
            body["role"] = "individual"
            body["name"] = p.get("name")
        elif kind == "feedback":
            body["name"] = p.get("name")
        db_put(f"{kind}/{uid}", body)
        sent += 1
    avg_len = sum(len(t) for t in texts) // len(texts)
    ok(f"נשלחו {sent} תגובות (אורך ממוצע: {avg_len} תווים)")


def wait_for_summary(kind):
    request_path, summary_path, label, button_label = PIPELINES[kind]
    pause(f"בדשבורד, לחץ על הכפתור: \"{button_label}\".")

    info(f"מחכה ל-/{summary_path}.status == 'ready' (timeout 180s)...")
    start = time.time()
    last_status = None
    while time.time() - start < 180:
        s = db_get(summary_path) or {}
        status = s.get("status")
        if status != last_status:
            elapsed = int(time.time() - start)
            info(f"  [{elapsed:>3}s] status = {status}")
            last_status = status
        if status == "ready":
            elapsed = int(time.time() - start)
            ok(f"סיכום '{label}' הגיע אחרי {elapsed} שניות")
            text = s.get("text", "")
            count = s.get("count", "?")
            print()
            print(f"--- סיכום ({len(text)} תווים, על בסיס {count} תגובות) ---")
            print(text)
            print("--- סוף סיכום ---")
            return True
        if status == "error":
            err(f"שגיאה בסיכום: {s.get('error', 'unknown')}")
            return False
        time.sleep(2)
    err(f"Timeout — הסיכום של '{label}' לא חזר תוך 180 שניות.")
    info("בדוק שה-summarize.py רץ בטרמינל אחר.")
    return False


