def db_url(path):
    return f"{DB_URL}/{path}.json"


def db_get(path):
    r = requests.get(db_url(path), timeout=10)
    return r.json() if r.status_code == 200 else None


def db_put(path, data):
    r = requests.put(db_url(path), json=data, timeout=10)
    r.raise_for_status()


def db_delete(path):
    requests.delete(db_url(path), timeout=10)


def now_ms():
    return int(time.time() * 1000)


